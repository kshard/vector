package pure

func EqualF32(a, b []float32) bool {
	if len(a) != len(b) {
		panic("vectors must have equal lengths")
	}

	for i := 0; i < len(a); i++ {
		if a[i] != b[i] {
			return false
		}
	}

	return true
}
